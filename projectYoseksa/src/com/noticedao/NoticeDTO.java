public class NoticeDTO {
	private int noticeno;
	private String noticesub;
	private int noticehire;
	private int noticehit;
	private String noticecont;
	private int boardno;
	public int getNoticeno() {
		return noticeno;
	}
	public void setNoticeno(int noticeno) {
		this.noticeno = noticeno;
	}
	public String getNoticesub() {
		return noticesub;
	}
	public void setNoticesub(String noticesub) {
		this.noticesub = noticesub;
	}
	public int getNoticehire() {
		return noticehire;
	}
	public void setNoticehire(int noticehire) {
		this.noticehire = noticehire;
	}
	public int getNoticehit() {
		return noticehit;
	}
	public void setNoticehit(int noticehit) {
		this.noticehit = noticehit;
	}
	public String getNoticecont() {
		return noticecont;
	}
	public void setNoticecont(String noticecont) {
		this.noticecont = noticecont;
	}
	public int getBoardno() {
		return boardno;
	}
	public void setBoardno(int boardno) {
		this.boardno = boardno;
	}
	
}
